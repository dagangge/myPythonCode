import turtle
turtle.setup(600,500)
turtle.penup()
turtle.goto(-100,-180)
turtle.pendown()
turtle.pensize(15)
turtle.pencolor("black")
turtle.fd(200)
turtle.seth(60)
turtle.fd(200)
turtle.left(60)
turtle.fd(200)
turtle.left(60)
turtle.fd(200)
turtle.left(60)
turtle.fd(200)
turtle.left(60)
turtle.fd(200)
turtle.left(60)
turtle.done()